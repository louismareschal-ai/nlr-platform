/**
 * Creates E2E test accounts in Supabase.
 * Run once: node scripts/create-e2e-users.mjs
 *
 * Requires SUPABASE_SERVICE_ROLE_KEY in .env.local.
 * Writes credentials to .env.test.
 */

import { createClient } from '@supabase/supabase-js'
import { readFileSync, writeFileSync, existsSync } from 'fs'
import { randomBytes } from 'crypto'

// Load .env.local manually
const envLocal = readFileSync('.env.local', 'utf8')
const env = Object.fromEntries(
  envLocal.split('\n')
    .filter(l => l.includes('='))
    .map(l => l.split('=').map(s => s.trim()))
)

const supabaseUrl = env['NEXT_PUBLIC_SUPABASE_URL']
const serviceRoleKey = env['SUPABASE_SERVICE_ROLE_KEY']

if (!supabaseUrl || !serviceRoleKey) {
  console.error('Missing NEXT_PUBLIC_SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY in .env.local')
  process.exit(1)
}

const supabase = createClient(supabaseUrl, serviceRoleKey, {
  auth: { autoRefreshToken: false, persistSession: false }
})

const TEST_USERS = [
  {
    email: 'e2e-super-admin@nlr-test.internal',
    role: 'super_admin',
    first_name: 'E2E',
    last_name: 'SuperAdmin',
    gender: 'man',
    envKey: 'E2E_SUPER_ADMIN',
  },
  {
    email: 'e2e-squad-admin@nlr-test.internal',
    role: 'squad_admin',
    first_name: 'E2E',
    last_name: 'SquadAdmin',
    gender: 'man',
    envKey: 'E2E_SQUAD_ADMIN',
  },
  {
    email: 'e2e-player@nlr-test.internal',
    role: 'player',
    first_name: 'E2E',
    last_name: 'Player',
    gender: 'woman',
    envKey: 'E2E_PLAYER',
  },
]

function makePassword() {
  return randomBytes(16).toString('base64url').slice(0, 20)
}

async function upsertUser(user) {
  // Check if auth user already exists
  const { data: existing } = await supabase.auth.admin.listUsers()
  const found = existing?.users?.find(u => u.email === user.email)

  let authUserId
  let password

  if (found) {
    // Reset password to a fresh one
    password = makePassword()
    const { error } = await supabase.auth.admin.updateUserById(found.id, { password })
    if (error) throw new Error(`Failed to reset password for ${user.email}: ${error.message}`)
    authUserId = found.id
    console.log(`  Updated password for existing user: ${user.email}`)
  } else {
    password = makePassword()
    const { data, error } = await supabase.auth.admin.createUser({
      email: user.email,
      password,
      email_confirm: true,
    })
    if (error) throw new Error(`Failed to create auth user ${user.email}: ${error.message}`)
    authUserId = data.user.id
    console.log(`  Created auth user: ${user.email} (${authUserId})`)
  }

  // Upsert players row
  const { error: playerError } = await supabase.from('players').upsert({
    id: authUserId,
    first_name: user.first_name,
    last_name: user.last_name,
    gender: user.gender,
    role: user.role,
    temp_password_changed: true, // skip forced password change in tests
  }, { onConflict: 'id' })

  if (playerError) throw new Error(`Failed to upsert player row for ${user.email}: ${playerError.message}`)
  console.log(`  Player row upserted with role=${user.role}`)

  return { email: user.email, password, envKey: user.envKey }
}

async function main() {
  console.log('Creating E2E test users...\n')

  const results = []
  for (const user of TEST_USERS) {
    console.log(`→ ${user.email} (${user.role})`)
    const result = await upsertUser(user)
    results.push(result)
    console.log()
  }

  // Write .env.test
  const lines = [
    '# E2E test credentials — generated by scripts/create-e2e-users.mjs',
    '# DO NOT COMMIT — listed in .gitignore',
    '',
    ...results.flatMap(r => [
      `${r.envKey}_EMAIL=${r.email}`,
      `${r.envKey}_PASSWORD=${r.password}`,
      '',
    ])
  ]

  writeFileSync('.env.test', lines.join('\n'))
  console.log('✓ .env.test written with fresh credentials')
  console.log('\nRun `npm run test:ui` to verify.')
}

main().catch(err => {
  console.error(err.message)
  process.exit(1)
})
